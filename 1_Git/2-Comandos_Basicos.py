'''
    Alguns conceitos:
        Branch:
        Staging:
        Commit:
        Push:
        Pull:

    Alguns comandos básicos e importantes:
        git init -> Cria um novo repositório Git em um diretório.
        git add -> Adiciona um arquivo à área de staging.
        git commit -m "Mensagem do commit": Cria um novo commit com a mensagem especificada.
        git status -> Mostra o status atual do repositório, incluindo arquivos modificados, não rastreados e em staging.
        git log -> Mostra o histórico de commits.
        git branch -> Lista todos os branches locais.
        git branch <nome_do_branch> -> Cria um novo branch.
        git checkout <nome_do_branch> -> Muda para um branch existente.
        git checkout -b <novo_branch> -> Cria um novo branch e muda para ele.
        git branch -d <nome_do_branch> -> Remove um branch local.
        git remote add origin <url> -> Adiciona um repositório remoto com o nome "origin".
        git push origin main -> Envia as alterações locais para o branch "main" do repositório remoto.
        git pull origin main -> Baixa as alterações do branch "main" do repositório remoto e as mescla no branch local.
        git clone <url> -> Clona um repositório existente.
        git diff: Mostra as diferenças entre os commits.
        git reset HEAD^ -> Desfaz o último commit.
        git revert <commit> -> Desfaz um commit específico.
        
'''